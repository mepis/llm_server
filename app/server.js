async function readJsonFile(filename) {
  try {
    const data = await fs.readFile(join(CONFIGS_DIR, filename), 'utf-8')
    return JSON.parse(data)
  } catch (error) {
    return []
  }
}

async function writeJsonFile(filename, data) {
  await fs.writeFile(join(CONFIGS_DIR, filename), JSON.stringify(data, null, 2))
}

async function generateBuildScript(config) {
  let script = '#!/bin/bash\n'
  script += '# Build Script for llama.cpp\n'
  script += `# Generated by Llama Manager\n\n`
  
  script += 'export CUDACXX=/usr/local/cuda/bin/nvcc\n'
  script += 'export GGML_CUDA_ENABLE_UNIFIED_MEMORY=1\n\n'
  
  if (config.envVars) {
    for (const [key, value] of Object.entries(config.envVars)) {
      if (value) {
        script += `export ${key}=${value}\n`
      }
    }
    script += '\n'
  }
  
  script += 'cmake -B build \\\n'
  
  const buildSharedLibs = config.buildSharedLibs !== undefined ? !config.buildSharedLibs : true
  script += `  -DBUILD_SHARED_LIBS=${buildSharedLibs ? 'ON' : 'OFF'} \\\n`
  
  const cmakeTypes = ['Debug', 'Release', 'RelWithDebInfo', 'MinSizeRel']
  const cmakeType = cmakeTypes.includes(config.cmakeBuildType) ? config.cmakeBuildType : 'RelWithDebInfo'
  script += `  -DCMAKE_BUILD_TYPE=${cmakeType} \\\n`
  
  if (config.ggmlCcache) script += '  -DGGML_CCACHE=ON \\\n'
  if (config.ggmlLto) script += '  -DGGML_LTO=ON \\\n'
  if (config.ggmlNative) script += '  -DGGML_NATIVE=ON \\\n'
  
  if (config.ggmlCuda) {
    script += '  -DGGML_CUDA=ON \\\n'
    if (config.ggmlCudaPeerMaxBatchSize) {
      script += `  -DGGML_CUDA_PEER_MAX_BATCH_SIZE=${config.ggmlCudaPeerMaxBatchSize} \\\n`
    }
    if (config.ggmlCudaFa) script += '  -DGGML_CUDA_FA=ON \\\n'
    if (config.ggmlCudaGraphs) script += '  -DGGML_CUDA_GRAPHS=ON \\\n'
    if (config.ggmlCudaForceMmq) script += '  -DGGML_CUDA_FORCE_MMQ=ON \\\n'
    if (config.ggmlCudaForceCublas) script += '  -DGGML_CUDA_FORCE_CUBLAS=ON \\\n'
  }
  
  if (config.ggmlHip) script += '  -DGGML_HIP=ON \\\n'
  if (config.ggmlVulkan) script += '  -DGGML_VULKAN=ON \\\n'
  if (config.ggmlMetal) script += '  -DGGML_METAL=ON \\\n'
  if (config.ggmlOpencl) script += '  -DGGML_OPENCL=ON \\\n'
  if (config.ggmlBlast) {
    script += '  -DGGML_BLAS=ON \\\n'
    const blasVendors = ['Generic', 'ATLAS', 'BLIS', 'OpenBLAS', 'MKL', 'ONEAPI', 'BLIS']
    const blasVendor = blasVendors.includes(config.ggmlBlastVendor) ? config.ggmlBlastVendor : 'Generic'
    script += `  -DGGML_BLAS_VENDOR=${blasVendor} \\\n`
  }
  if (config.ggmlCann) script += '  -DGGML_CANN=ON \\\n'
  if (config.ggmlZendnn) script += '  -DGGML_ZENDNN=ON \\\n'
  if (config.ggmlKleidiai) script += '  -DGGML_CPU_KLEIDIAI=ON \\\n'
  
  script = script.replace(/ \\\n$/, '\n')
  script += 'cmake --build build --config ${CMAKE_BUILD_TYPE:-RelWithDebInfo} --parallel\n'
  
  return script
}

async function generateRunScript(config) {
  let script = '#!/bin/bash\n'
  script += '# Run Script for llama.cpp\n'
  script += `# Generated by Llama Manager\n\n`
  
  if (config.envVars) {
    script += 'export LLAMA_CACHE=${LLAMA_CACHE:-./cache}\n'
    for (const [key, value] of Object.entries(config.envVars)) {
      if (value) {
        script += `export ${key}=${value}\n`
      }
    }
    script += '\n'
  }
  
  script += './llama-server \\\n'
  
  if (config.modelPath) {
    script += `  -m "${config.modelPath}" \\\n`
  }
  
  if (config.contextSize) {
    script += `  -c ${config.contextSize} \\\n`
  }
  
  if (config.threads) {
    script += `  -t ${config.threads} \\\n`
  }
  
  if (config.threadsBatch) {
    script += `  -tb ${config.threadsBatch} \\\n`
  }
  
  if (config.batchSize) {
    script += `  -b ${config.batchSize} \\\n`
  }
  
  if (config.ubatchSize) {
    script += `  -ub ${config.ubatchSize} \\\n`
  }
  
  if (config.gpuLayers) {
    if (config.gpuLayers === 'all') {
      script += '  -ngl all \\\n'
    } else {
      script += `  -ngl ${config.gpuLayers} \\\n`
    }
  }
  
  if (config.mlock) script += '  --mlock \\\n'
  if (config.mmap) script += '  --mmap \\\n'
  if (config.directIo) script += '  --direct-io \\\n'
  
  if (config.numa) {
    const numaModes = ['off', 'bind', 'spread']
    const numaMode = numaModes.includes(config.numa) ? config.numa : 'off'
    script += `  --numa ${numaMode} \\\n`
  }
  
  if (config.contBatching) script += '  --cont-batching \\\n'
  if (config.kvUnified) script += '  --kv-unified \\\n'
  if (config.flashAttn) script += '  --flash-attn \\\n'
  
  if (config.cacheTypeK) {
    const cacheTypes = ['f16', 'q8_0', 'q4_0']
    const cacheTypeK = cacheTypes.includes(config.cacheTypeK) ? config.cacheTypeK : 'f16'
    script += `  --cache-type-k ${cacheTypeK} \\\n`
  }
  
  if (config.cacheTypeV) {
    const cacheTypes = ['f16', 'q8_0', 'q4_0']
    const cacheTypeV = cacheTypes.includes(config.cacheTypeV) ? config.cacheTypeV : 'f16'
    script += `  --cache-type-v ${cacheTypeV} \\\n`
  }
  
  if (config.splitMode) {
    const splitModes = ['none', 'layer', 'row']
    const splitMode = splitModes.includes(config.splitMode) ? config.splitMode : 'none'
    script += `  --split-mode ${splitMode} \\\n`
  }
  
  if (config.tensorSplit) {
    script += `  --tensor-split ${config.tensorSplit} \\\n`
  }
  
  if (config.mainGpu !== undefined && config.mainGpu !== null) {
    script += `  --main-gpu ${config.mainGpu} \\\n`
  }
  
  if (config.temperature) {
    script += `  --temp ${config.temperature} \\\n`
  }
  
  if (config.topK) {
    script += `  --top-k ${config.topK} \\\n`
  }
  
  if (config.topP) {
    script += `  --top-p ${config.topP} \\\n`
  }
  
  if (config.minP) {
    script += `  --min-p ${config.minP} \\\n`
  }
  
  if (config.typicalP) {
    script += `  --typical-p ${config.typicalP} \\\n`
  }
  
  if (config.repeatLastN) {
    script += `  --repeat-last-n ${config.repeatLastN} \\\n`
  }
  
  if (config.repeatPenalty) {
    script += `  --repeat-penalty ${config.repeatPenalty} \\\n`
  }
  
  if (config.presencePenalty) {
    script += `  --presence-penalty ${config.presencePenalty} \\\n`
  }
  
  if (config.frequencyPenalty) {
    script += `  --frequency-penalty ${config.frequencyPenalty} \\\n`
  }
  
  if (config.mirostat) {
    const mirostatModes = [0, 1, 2]
    const mirostatMode = mirostatModes.includes(config.mirostat) ? config.mirostat : 0
    script += `  --mirostat ${mirostatMode} \\\n`
  }
  
  if (config.grammar) {
    script += `  --grammar "${config.grammar}" \\\n`
  }
  
  if (config.jsonSchema) {
    script += `  --json-schema "${config.jsonSchema}" \\\n`
  }
  
  if (config.host) {
    script += `  --host ${config.host} \\\n`
  }
  
  if (config.port) {
    script += `  --port ${config.port} \\\n`
  }
  
  if (config.parallel) {
    script += `  --parallel ${config.parallel} \\\n`
  }
  
  if (config.webui) script += '  --webui \\\n'
  if (config.apiKey) {
    script += `  --api-key "${config.apiKey}" \\\n`
  }
  if (config.metrics) script += '  --metrics \\\n'
  if (config.timeout) {
    script += `  --timeout ${config.timeout} \\\n`
  }
  
  if (config.loraPath) {
    script += `  --lora "${config.loraPath}" \\\n`
  }
  
  if (config.mmprojPath) {
    script += `  --mmproj "${config.mmprojPath}" \\\n`
  }
  
  script = script.replace(/ \\\n$/, '\n')
  script += '\n'
  
  return script
}

