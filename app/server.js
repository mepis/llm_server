app.get('/api/scripts/list', async (req, res) => {
  const scripts = []
  
  try {
    const buildScripts = await fs.readdir(join(SCRIPTS_DIR, 'builds'))
    for (const file of buildScripts) {
      if (file.endsWith('.sh')) {
        scripts.push({
          id: file.replace('.sh', ''),
          name: file.replace('.sh', ''),
          type: 'build',
          path: join(SCRIPTS_DIR, 'builds', file)
        })
      }
    }
  } catch (error) {}
  
  try {
    const runScripts = await fs.readdir(join(SCRIPTS_DIR, 'runs'))
    for (const file of runScripts) {
      if (file.endsWith('.sh')) {
        scripts.push({
          id: file.replace('.sh', ''),
          name: file.replace('.sh', ''),
          type: 'run',
          path: join(SCRIPTS_DIR, 'runs', file)
        })
      }
    }
  } catch (error) {}
  
  res.json(scripts)
})

app.get('/api/scripts/:id', async (req, res) => {
  const scriptPath = join(SCRIPTS_DIR, req.query.type === 'build' ? 'builds' : 'runs', `${req.params.id}.sh`)
  try {
    const content = await fs.readFile(scriptPath, 'utf-8')
    res.json({ id: req.params.id, content })
  } catch (error) {
    res.status(404).json({ error: 'Script not found' })
  }
})

app.post('/api/scripts/execute', async (req, res) => {
  const { scriptPath, args = [] } = req.body
  if (!scriptPath) {
    return res.status(400).json({ error: 'Script path required' })
  }
  
  try {
    await fs.access(scriptPath)
  } catch (error) {
    return res.status(404).json({ error: 'Script not found' })
  }
  
  const scriptDir = scriptPath.substring(0, scriptPath.lastIndexOf('/'))
  const scriptName = scriptPath.substring(scriptPath.lastIndexOf('/') + 1)
  
  exec(`chmod +x "${scriptPath}" && cd "${scriptDir}" && ./${scriptName} ${args.join(' ')}`, {
    maxBuffer: 10 * 1024 * 1024
  }, (error, stdout, stderr) => {
    if (error) {
      res.json({ 
        success: false, 
        error: error.message,
        stderr: stderr || ''
      })
      return
    }
    res.json({ 
      success: true, 
      output: stdout,
      stderr: stderr || ''
    })
  })
})

app.post('/api/scripts/save', async (req, res) => {
  const { name, type, content } = req.body
  if (!name || !type || !content) {
    return res.status(400).json({ error: 'name, type, and content required' })
  }
  
  const dir = join(SCRIPTS_DIR, type === 'build' ? 'builds' : 'runs')
  const filename = `${name.replace(/[^a-zA-Z0-9_-]/g, '-')}.sh`
  const filepath = join(dir, filename)
  
  await fs.mkdir(dir, { recursive: true })
  await fs.writeFile(filepath, content)
  
  res.json({ id: name.replace(/[^a-zA-Z0-9_-]/g, '-'), path: filepath })
})

app.delete('/api/scripts/:id', async (req, res) => {
  const { id } = req.params
  const type = req.query.type || 'run'
  
  const filepath = join(SCRIPTS_DIR, type === 'build' ? 'builds' : 'runs', `${id}.sh`)
  
  try {
    await fs.unlink(filepath)
    res.json({ success: true })
  } catch (error) {
    res.status(404).json({ error: 'Script not found' })
  }
})

